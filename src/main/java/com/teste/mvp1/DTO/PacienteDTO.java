package com.teste.mvp1.DTO;

public class PacienteDTO {

    private String nome;
    private long cpf;
    private int idade;
    private String endereco;
    private long fone;
    private String email;
    private String convenio;

    public String getNome() {
        return nome;
    }

    public long getCpf() {
        return cpf;
    }

    public int getIdade() {
        return idade;
    }

    public String getEndereco() {
        return endereco;
    }

    public long getFone() {
        return fone;
    }

    public String getEmail() {
        return email;
    }

    public String getConvenio() {
        return convenio;
    }

}
